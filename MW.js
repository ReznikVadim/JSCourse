// "Причесываем" номер
for (const user of users) {
  if (typeof user.phone === "string") {
    const cleanedPhone = user.phone.replace(/[^0-9]/g, "");

    if (!cleanedPhone.startsWith("000")) {
      user.phone = cleanedPhone;
    } else {
      user.phone = null;
    }
  } else {
    user.phone = null;
  }
}

// Сортируем пользователей по алфавиту по полю name
const sortedUsers = users.sort((a, b) => a.name.localeCompare(b.name));

console.log(sortedUsers);

// Находим дальнеживущего по полю suite
let farthestUser;
let maxSuiteNumber = -Infinity;

for (const user of users) {
  const suiteNumber = parseInt(user.address.suite.split(" ")[1]);

  if (!isNaN(suiteNumber) && suiteNumber > maxSuiteNumber) {
    maxSuiteNumber = suiteNumber;
    farthestUser = user;
  }
}

